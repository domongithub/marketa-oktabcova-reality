  sreality_last_photo_sync_at timestamptz,
  sreality_error text,
  sreality_payload_preview jsonb,
  sreality_response_log jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  published_at timestamptz,
  sold_at timestamptz,
  rented_at timestamptz,
  archived_at timestamptz
);

create table if not exists property_images (
  id uuid primary key default gen_random_uuid(),
  property_id uuid not null references properties(id) on delete cascade,
  url text not null,
  storage_path text,
  alt text,
  sort_order integer not null default 0,
  is_main boolean not null default false,
  sreality_photo_id text,
  sreality_photo_rkid text,
  sreality_photo_kind integer,
  sreality_room_type integer,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists sreality_export_logs (
  id uuid primary key default gen_random_uuid(),
  property_id uuid references properties(id) on delete set null,
  action text not null check (action in ('validate','dry_run','export_advert','update_advert','remove_advert','add_photo','delete_photo','sync_photos','list_adverts')),
  status text not null,
  request_payload jsonb,
  response_payload jsonb,
  status_code integer,
  status_message text,
  error_message text,
  performed_by uuid references admin_users(id) on delete set null,
  created_at timestamptz not null default now()
);

create index if not exists properties_public_idx on properties(status, show_on_website);
create index if not exists properties_slug_idx on properties(slug);
create index if not exists property_images_property_order_idx on property_images(property_id, sort_order);
create index if not exists sreality_logs_property_idx on sreality_export_logs(property_id, created_at desc);

insert into storage.buckets (id, name, public)
values ('property-images', 'property-images', true)
on conflict (id) do nothing;
